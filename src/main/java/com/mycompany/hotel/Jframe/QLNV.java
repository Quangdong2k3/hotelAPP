    private void init_CB_role() {
        roleBoxModel = new DefaultComboBoxModel();
        List<Role> lst = new RoleImpDAO().getAllRole();
        cbRole.addItem("Chọn quyền hạn");
        for (Role r : lst) {

            cbRole.addItem(r.getId() + " - " + r.getName());
        }

    }

    private int getIdRole() {
        int i = 0;
        if (cbRole.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "bạn vui lòng chọn quyền cho người dùng!!!");
        } else {
            String[] arr = cbRole.getSelectedItem().toString().split("-");

            i = Integer.parseInt(arr[0].toString().trim());
            JOptionPane.showMessageDialog(null, i);

        }
        return i;

    }

