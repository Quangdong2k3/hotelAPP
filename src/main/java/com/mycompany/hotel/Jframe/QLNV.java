    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handli ng code here:

        int role = getIdRole();
        String ten = txtten.getText();
        String tk = txttk.getText();
        String mk = txtmk.getText();
        String md5Hex = DigestUtils.md5Hex(mk).toUpperCase();
        Employee r = new Employee(new Role(role, "", ""), tk, md5Hex, 0, ten);

        boolean success = new EmpImlDAO().insert(r);
        if (success) {
            reset();
            JOptionPane.showMessageDialog(null, "Thêm thành công");
            loadTBL();
        } else {
            JOptionPane.showMessageDialog(null, "Thất bại");
        }

    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:

        try {
            if (txtId.getText().equalsIgnoreCase("")) {
                JOptionPane.showMessageDialog(null, "Vui lòng nhập mã khách hàng");
                return;
            }
            int id = Integer.parseInt(txtId.getText());
            if (id > 0) {
                int check = JOptionPane.showConfirmDialog(null, "Bạn có muốn xóa không", "Xóa", JOptionPane.WARNING_MESSAGE, JOptionPane.OK_CANCEL_OPTION);

                boolean success = check == JOptionPane.OK_OPTION ? new EmpImlDAO().delete(id) : false;

                if (success) {
                    reset();
                    JOptionPane.showMessageDialog(null, "Xóa thành công");
                    loadTBL();
                } else {
                    JOptionPane.showMessageDialog(null, "Thất bại");
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());

        }
    }//GEN-LAST:event_btnDeleteActionPerformed

