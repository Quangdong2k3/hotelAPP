    private void LoadGuiRoom() {
        
        FlowLayout flowLayout = new FlowLayout();
        JPanel panel = new JPanel();
        panel.setSize(jDesktopPane1.getWidth(), jDesktopPane1.getWidth());
        panel.setLayout(flowLayout);
        flowLayout.setAlignment(FlowLayout.LEFT);
        flowLayout.setHgap(15);
        flowLayout.setVgap(15);
        RoomImpDao rimlp = new RoomImpDao();
        List<Room> lst = rimlp.getAllRoom();
        for (Room r : lst) {
            String name = "Phòng " + String.valueOf(r.getSoPhong());
            JButton addButton = new JButton(name);
            addButton.setFont(new java.awt.Font("Tahoma", 2, 18)); // NOI18N
            addButton.setActionCommand(String.valueOf(r.getSoPhong()));
            addButton.setName(String.valueOf(r.getId()));
            addButton.setPreferredSize(new Dimension(300, 250));
            addButton.setIcon(new javax.swing.ImageIcon(getClass().getResource("/img/living-room (1).png"))); // NOI18N
            if (r.isStatus()) {
                addButton.setEnabled(false);
                addButton.setBackground(Color.RED);
                addButton.setOpaque(true);
            } else {
                addButton.setBackground(new java.awt.Color(102, 255, 102));
                addButton.addActionListener(new ActionListener() {
                    @Override
                    public void actionPerformed(ActionEvent e) {
                        JButton nut = (JButton) e.getSource();
//                        JOptionPane.showMessageDialog(rootPane, nut.getName());
                        ThongtinDAtphong dp = new ThongtinDAtphong(nut.getName(), "Phòng " + nut.getActionCommand(), String.valueOf(JFLogin.id_nv));
                        dp.setVisible(true);
                    }
                });
                
            }
            panel.add(addButton);
            
        }
        jPanel1.add(panel);
    }

