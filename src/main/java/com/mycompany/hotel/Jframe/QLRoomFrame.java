    private void btnDeleteRoomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteRoomActionPerformed
        // TODO add your handling code here:

        try {
            if (txtIdR.getText().equalsIgnoreCase("")) {
                JOptionPane.showMessageDialog(null, "Vui lòng nhập mã phòng");
                return;
            }
            int id = Integer.parseInt(txtIdR.getText());
            if (id > 0) {
                int check = JOptionPane.showConfirmDialog(null, "Bạn có muốn xóa không", "Xóa", JOptionPane.WARNING_MESSAGE, JOptionPane.OK_CANCEL_OPTION);
                
                if (check == JOptionPane.OK_OPTION) {
                    boolean success = new RoomImpDao().delete(id);
                    
                    if (success) {
                        reset();
                        JOptionPane.showMessageDialog(null, "Xoa thành công");
                        loadTBL();
                    } else {
                        JOptionPane.showMessageDialog(null, "Thất bại");
                    }
                } else {
                    return;
                }
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
            
        }
    }//GEN-LAST:event_btnDeleteRoomActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        if(index<0){
            return;
        }
        int id = Integer.parseInt(jTable1.getValueAt(index, 0).toString().trim());
        int sophong = Integer.parseInt(jTable1.getValueAt(index, 1).toString().trim());
        int sotang = Integer.parseInt(jTable1.getValueAt(index, 2).toString().trim());
        int sogiuong = Integer.parseInt(jTable1.getValueAt(index, 3).toString().trim());
        double gia = Double.parseDouble(jTable1.getValueAt(index, 4).toString().trim());
        boolean c = false;
        if (jTable1.getValueAt(index, 5).toString().trim().equals("Còn")) {
            c = false;
        } else {
            c = true;
        }
        //        JOptionPane.showMessageDialog(null, id);

        Room r = new Room(id, sophong, sotang, sogiuong, gia, c);
        dolaidulieu(r);
    }//GEN-LAST:event_jTable1MouseClicked
    
    private void loadTBL() {
        
        lst = new RoomImpDao().getAllRoom();
        tbl = new DefaultTableModel();
        tbl.addColumn("Mã Phòng");
        tbl.addColumn("Số Phòng");
        tbl.addColumn("Số Tầng");
        tbl.addColumn("Số Giường");
        tbl.addColumn("Giá");
        tbl.addColumn("Tình Trạng");
        jTable1.setModel(tbl);
        
        for (Room r : lst) {
            String [] arrom = {String.valueOf(r.getId()),String.valueOf(r.getSoPhong()),String.valueOf(r.getSoTang()),String.valueOf(r.getSoGiuong()),String.valueOf(r.getGia()),r.isStatus() ? "Đã đặt" : "Còn"};
          
            tbl.addRow(arrom);
        }
        jTable1.setModel(tbl);
        
    }

