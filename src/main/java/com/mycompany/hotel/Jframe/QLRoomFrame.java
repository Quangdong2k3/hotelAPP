    private void dolaidulieu(Room r) {
        txtIdR.setText(String.valueOf(r.getId()));
        txtGia.setText(String.valueOf(r.getGia()));
        txtNameR.setText(String.valueOf(r.getSoPhong()));
        txtSoGiuong.setText(String.valueOf(r.getSoGiuong()));
        txtSoTang.setText(String.valueOf(r.getSoTang()));
        txtIdR.setEnabled(false);
        if (r.isStatus()) {
            jRadioButton2.setSelected(true);
            
            jRadioButton1.setSelected(false);
        } else {
            jRadioButton2.setSelected(false);
            jRadioButton1.setSelected(true);
            
        }
    }
    
    private void reset() {
        txtIdR.setText("");
        txtGia.setText("");
        txtNameR.setText("");
        txtSoGiuong.setText("");
        txtSoTang.setText("");
        txtNameR.setText("");
        txtNameR.requestFocus();
        txtIdR.setEnabled(true);
        buttonGroup1.clearSelection();
        loadTBL();
    }
    private void btnAddRoomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddRoomActionPerformed
        // TODO add your handling code here:
        try {
            int sophong = Integer.parseInt(txtNameR.getText());
            int sotang = Integer.parseInt(txtSoTang.getText());
            double gia = Double.parseDouble(txtGia.getText());
            int sogiuong = Integer.parseInt(txtSoGiuong.getText());
            boolean isCheck = false;
            if (jRadioButton1.isSelected()) {
                isCheck = false;
            }
            if (jRadioButton2.isSelected()) {
                isCheck = true;
            }
            Room r = new Room();
            r.setSoPhong(sophong);
            r.setSoTang(sotang);
            r.setSoGiuong(sogiuong);
            r.setGia(gia);
            r.setStatus(isCheck);
            boolean success = new RoomImpDao().insert(r);
            if (success) {
                reset();
                JOptionPane.showMessageDialog(null, "THêm thành công");
                loadTBL();
            } else {
                JOptionPane.showMessageDialog(null, "Thất bại");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnAddRoomActionPerformed

