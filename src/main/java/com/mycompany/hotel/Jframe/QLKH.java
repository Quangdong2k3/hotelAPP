    private void reset() {
        txtIdKh.setText("");
        txtIdKh.setEnabled(true);
        txtCCCD.setText("");
        txtSdt.setText("");
        txttenKh.setText("");
    }
    private List<KhachHang> lstKh;
    private DefaultTableModel tblModel;

    private void loadTBL() {
        tblModel = new DefaultTableModel();
        tblModel.addColumn("ID");
        tblModel.addColumn("Tên");
        tblModel.addColumn("CCCD");
        tblModel.addColumn("SĐT");
        tblKh.setModel(tblModel);

        lstKh = new KHImpDao().getAllKH();
        for (KhachHang kh : lstKh) {
            Vector v = new Vector();
            v.add(kh.getId());
            v.add(kh.getTen());
            v.add(kh.getCccd());
            v.add(kh.getSdt());
            tblModel.addRow(v);
        }
        tblKh.setModel(tblModel);
    }

    private void dolaidulieu(KhachHang r) {
        txtIdKh.setText(String.valueOf(r.getId()));
        txtIdKh.setEnabled(false);
        txtCCCD.setText(r.getCccd());
        txtSdt.setText(r.getSdt());
        txttenKh.setText(r.getTen());
    }
